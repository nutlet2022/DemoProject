package com.movie.entity;

public class PoMovie {
    private String Title;
    private String Genres;


    public void setTitle(String title) {
        Title = title;
    }

    public void setGenres(String genres) {
        Genres = genres;
    }

    public String getTitle() {
        return Title;
    }

    public String getGenres() {
        return Genres;
    }
}


